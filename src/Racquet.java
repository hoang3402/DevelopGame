public class Racquet {
    int locationX;
    int locationY;
    int height;
    int width;
    int speed;

    public Racquet(int locationX, int locationY, int height, int width, int speed) {
        this.locationX = locationX;
        this.locationY = locationY;
        this.height = height;
        this.width = width;
        this.speed = speed;
    }
}
